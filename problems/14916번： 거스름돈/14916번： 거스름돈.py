INF = float("inf")
N=int(input())
 
dp = [INF] * (100_001)
dp[2] = 1 
dp[4] = 2 
dp[5] = 1 
 
for i in range(6, N+1):
    dp[i] = min(dp[i-2], dp[i-5]) + 1 
print(dp[N] if dp[N] != INF else -1)