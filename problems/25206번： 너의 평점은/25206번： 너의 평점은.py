s_dict= {
    "A+" : 4.5,
    "A0" : 4.0,
    "B+" : 3.5,
    "B0" : 3.0,
    "C+" : 2.5,
    "C0" : 2.0,
    "D+" : 1.5,
    "D0" : 1.0,
    "F"  : 0.0
}
 
n_total = 0
s_total = 0
for _ in range(20):
    _, n, s = input().split()
    if s == "P":
        continue
    else:
        n =float(n)
        n_total += n
        s_total += n * s_dict[s]
        
print(s_total / n_total)
 