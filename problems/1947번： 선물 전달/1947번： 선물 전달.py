N = int(input())
MOD = 1_000_000_000
 
# 초기 조건
if N < 2:
    ans =  0
else:
    dp = [0] * (N+1)
    dp[0], dp[1] = 1, 0
    
    # 완전순열의 수 계산
    for i in range(2, N+1):
        dp[i] = ((i - 1) * (dp[i-1] + dp[i-2])) % MOD
    ans = dp[-1] 
print (ans)