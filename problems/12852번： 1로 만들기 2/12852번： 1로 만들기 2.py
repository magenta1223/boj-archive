N=int(input().strip())
H = [[1]] * (N+1)
dp = [float("inf")] * (N+1)
dp[1] = 0
for i in range(1, N):
    for j in [i+1, 2*i, 3*i]:
        if 0<j<=N:
            if dp[i] + 1 < dp[j]:
                dp[j] = dp[i] + 1
                H[j] = H[i] + [j]
print(dp[-1])
print(*H[-1][::-1])