MOD = 10007
N,K = map(int,input().split())
dp = [[0] * (K+1) for _ in range(N+1)]
dp[0][0] = 0
for i in range(N+1):
    dp[i][0] = 1 
for n in range(1,N+1):
    for k in range(1,min(K,n)+1):
        dp[n][k] = (dp[n-1][k-1] + dp[n-1][k]) % MOD
print(dp[-1][-1])