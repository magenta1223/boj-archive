N = int(input())
for i in range(2 * N - 1):
    if i >= N - 1:
        print(  " "*(i-(N - 1)) + "*" * (4 * (N - 1) - 2* i+ 1))
    else:
        print(  " "*(N-i-1) + "*" * (2 * i + 1))