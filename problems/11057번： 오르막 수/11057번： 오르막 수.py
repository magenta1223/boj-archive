MOD = 10_007
N=int(input())
dp = [[0] * 10 for _ in range(N)]
dp[0] = [1] * 10 
for i in range(1,N):
    dp[i][0] = 1 
    for j in range(1,10):
        dp[i][j] = (dp[i][j-1] + dp[i-1][j]) % MOD 
print(sum(dp[-1]) % MOD)