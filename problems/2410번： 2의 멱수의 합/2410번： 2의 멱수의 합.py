def solve(n):
    dp = [0 for _ in range(n+1)]    
    dp[0] = 1
    dp[1] = 1 
    for i in range(2,n+1):
        if i % 2:
            dp[i] = dp[i-1]
            continue
        # dp[i] = sum(dp[:i//2+1]) % 1_000_000_000
        dp[i] = (dp[i-2] + dp[i//2]) % 1_000_000_000 
        
 
    return dp[-1]
 
 
