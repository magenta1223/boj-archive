MOD = 10_007 
N = int(input())
if N == 1:
    print(1)
    exit(0)
 
# 2xN을 채우는 방법 
dp = [0] * N 
dp[0] = 1
dp[1] = 3
for i in range(2,N):
    dp[i] = (dp[i-2] * 2 + dp[i-1]) 
print(dp[-1]%MOD)